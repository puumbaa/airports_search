package com.renue.internship.app.version_2;

public class PointerCouple {
    private final int start;
    private final int end;

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }

    public PointerCouple(int start, int end) {
        this.start = start;
        this.end = end;
    }
}
